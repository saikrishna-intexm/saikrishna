    <script>
        document.addEventListener('DOMContentLoaded', function () {
            var form = document.querySelector('.registration-form');
            var numberInput = document.getElementById('number');
            var pincodeInput = document.getElementById('pincode');
            var employmentInputs = document.querySelectorAll('input[name="self-salary"]');
            var agreeCheckbox = document.getElementById('agree');
            var applyButton = document.querySelector('button[type="submit"]');

            // Function to check if all fields are filled and correct
            function checkFields() {
                var isNumberValid = numberInput.value.length === 10 && !isNaN(numberInput.value);
                var isPincodeValid = pincodeInput.value.length === 6 && !isNaN(pincodeInput.value);
                var isEmploymentSelected = Array.from(employmentInputs).some(input => input.checked);
                var isAgreementChecked = agreeCheckbox.checked;

                return isNumberValid && isPincodeValid && isEmploymentSelected && isAgreementChecked;
            }

            // Function to enable/disable the button based on field values
            function updateButton() {
                applyButton.disabled = !checkFields();
            }

            // Event listeners for input changes
            numberInput.addEventListener('input', updateButton);
            pincodeInput.addEventListener('input', updateButton);
            agreeCheckbox.addEventListener('change', updateButton);
            employmentInputs.forEach(function (input) {
                input.addEventListener('change', updateButton);
            });

            // Event listener for form submission
            form.addEventListener('submit', function (event) {
                event.preventDefault(); // Prevent form submission

                // Check if all fields are filled and correct
                if (!checkFields()) {
                    alert('Please fill in all fields correctly.');
                    return;
                }

                // If all validations pass, redirect to success page
                window.location.href = 'success.html';
            });

            // Initial button state
            updateButton();
        });
    </script>




