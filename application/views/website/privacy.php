                <p><strong>b. KYC Data:</strong> Identification documents issued by the government or other authorities, such as Aadhaar, PAN card, voter ID, etc.</p>

                <p><strong>c. Transaction Data:</strong> Details of transactions that occur through the Platform or in connection with the Services. For example, transaction data may include services you've sought or availed through the Platform or confirmations
                    of such services.</p>

                <p><strong>d. Data from SMS:</strong> With your permission, we collect, transmit, and store SMS data from your phone on our secure Low Credit server to verify and analyze your financial position, determine your cash flow, credits, income,
                    and spending pattern.</p>

                <p><strong>e. Financial Data:</strong> Past credit history, income details, details of loans issued or applied for through the Platform, payments, repayments, bank account details, and bank account statements.</p>

                <p><strong>f. Device Data:</strong> IP addresses, browser types and versions, time zone settings, operating systems, and device information, all collected, transmitted, and stored on our secure Low Credit server.</p>

                <p><strong>g. App Data:</strong> We collect, transmit, and store information about the installed applications on our secure Low Credit server. We only use the package name of each installed application to assess creditworthiness and provide
                    customized loan offers.</p>


                <p><strong>2.2.</strong>We do not access resources on your mobile phone such as contact lists, call logs, telephony functions, files, and media, except as disclosed to enable you to upload documents. We do not collect biometric data. However,
