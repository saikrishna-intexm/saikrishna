                    data, or when reporting issues with the Platform and Services. We keep records of correspondence when you contact us.</p>

                <p><strong>b. Information We Collect Automatically:</strong> Each time you visit the Platform or use the Services, we automatically collect personal data through tools like cookies.</p>

                <p><strong>c. Information from Third Parties:</strong> We receive personal data from third parties and publicly available sources, including Google analytics for advertising and user analytics purposes.</p>


